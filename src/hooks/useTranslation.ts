export const useTranslation = () => {
  const [language, setLanguage] = useState<Language>("ko");

  useEffect(() => {
    // Get saved language from localStorage
    const savedLang = localStorage.getItem("language") as Language;
    if (savedLang && translations[savedLang]) {
      setLanguage(savedLang);
    } else {
      // Detect browser language
      const browserLang = navigator.language.split("-")[0];
      const supportedLang = Object.keys(translations).find((lang) =>
        lang.startsWith(browserLang)
      ) as Language;

      if (supportedLang) {
        setLanguage(supportedLang);
      }
    }

    // Listen for language change events
    const handleLanguageChange = (event: CustomEvent) => {
      setLanguage(event.detail);
    };

    window.addEventListener(
      "languageChange",
      handleLanguageChange as EventListener
    );

    return () => {
      window.removeEventListener(
        "languageChange",
        handleLanguageChange as EventListener
      );
    };
  }, []);

  const t = (key: TranslationKey, params?: Record<string, string>): string => {
    const value = translations[language][key];

    if (typeof value !== "string") {
      return key;
    }

    // Replace parameters like {{provider}}
    if (params) {
      return Object.entries(params).reduce((text, [param, val]) => {
        return text.replace(new RegExp(`{{${param}}}`, "g"), val);
      }, value);
    }

    return value;
  };

  const translateLocation = (locationName: string): string => {
    // Location names are already in Korean, return as-is
    return locationName;
  };

  return { t, language, translateLocation };
};
